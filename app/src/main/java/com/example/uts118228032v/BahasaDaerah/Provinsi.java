package com.example.uts118228032v.BahasaDaerah;

public class Provinsi {
    private Integer id;
    private String itemprovinsi;
    private Deskripsi deskripsi;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getItemprovinsi() {
        return itemprovinsi;
    }

    public void setItemprovinsi(String itemprovinsi) {
        this.itemprovinsi = itemprovinsi;
    }

    public Deskripsi getDeskripsi() {
        return deskripsi;
    }

    public void setDeskripsi(Deskripsi deskripsi) {
        this.deskripsi = deskripsi;
    }
}
